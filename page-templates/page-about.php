<p><strong>Beagle Freedom Project</strong>, founded in 2010, is a unique program run by the Los Angeles-based non-profit Animal Rescue, Media, and Education (ARME). Beagle Freedom Project negotiates with laboratories across the world to secure the release of dogs and other animals and give them a chance at a normal home life after everything they have endured for vanity and scientific curiosity. </p>
<p>
The organization uses the rescues to draw public attention to the plight of animals languishing in labs and to promote cruelty-free lifestyle choices everyone can make. The beagles act as ambassadors who give faces to animal testing victims and remind the public that these animals are not just furry test-tubes, but dogs no different from their pets.</p>
<p>
To date, Beagle Freedom Project has rescued over 500 beagles, mixed-breed hounds, cats, rabbits, ponies, pigs, goats, guinea pigs, rats, and even goldfish from laboratories. Beagle Freedom Project has given hope and homes to these research survivors in 35 states and 6 countries. Their stories of recovery have been covered extensively by local and national media, such as CNN, NBC Nightly News, HLN, The L.A. Times, The San Francisco Chronicle, Yahoo! News, The Huffington Post, The Washington Post, and many more.</p>
<p>
Beagle Freedom Project has an ambitious immediate and long-term agenda that is both political and pragmatic. The organization is working toward the release of more animals and an end to animal testing altogether. </p>



