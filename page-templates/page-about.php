       <h2 class="subpage-featurette-heading text-center text-muted">Electronic Press Kit</h2>
        <div class="row">
            <p>
            Beagle Freedom Project has an ambitious immediate and long-term agenda that is both political and pragmatic. The organization is working toward the release of more animals and an end to animal testing altogether. To actualize this life-saving agenda, the organization focuses on four key programs:
            </p>
           <div class="col-md-6">
           <p>  <span class="background-paw"></span><strong>Rescue and Outreach Center (ROC)</strong>: Often, dogs emerging from prolonged periods of captivity and research violence need special care to address their physical and emotional needs. BFP is constructing a Los Angeles center to initially home these dogs, and to serve as a headquarters and outreach / educational center for the public and press.</p> 
            <p><img class="img-responsive center-block" src="/wp-content/themes/bfp/img/about/about_roc.jpg" alt="Rescue and Outreach Center (ROC)" /></p>
           </div>
           <div class="col-md-6">
           <p>  <span class="background-paw"></span> <strong>Legislation</strong>: There are 383 labs in the U.S. that use nearly 70,000 dogs every year (of which 96% are beagles). Because so many labs kill the dogs instead of retiring them, BFP is advancing legislation to mandate their release, which will guarantee their freedom and attract positive press attention to this hidden, problematic reality. BFP made history passing this bill into law in California, Minnesota, Connecticut, Nevada, and New York. It is currently pending in Illinois and Maryland.</p>
           </div> 
            <div class="col-md-6">
           <p>  <span class="background-paw"></span><strong>Rescues</strong>: BFP has rescue opportunities on the horizon from labs across the U.S., the UK, Europe, Korea, and even Australia. Each survivor is a living reminder that animals in labs are not abstract. </p>
           </div>
           <div class="col-md-6">
           <p>  <span class="background-paw"></span><strong>Education</strong>: The use of beagles in labs and all animal testing will only end once people are informed. BFP runs nationwide outreach / educational programs to raise awareness of the plight of lab animals and present steps people can take to help stop testing. Via efforts like the <a href="http://www.identitycampaign.org" target="_blank">Identity Campaign</a>, classroom visits, and the BFP Kids Program more and more citizens are learning about animal testing and the non-violent alternatives. </p>
       		</div>
            <div class="col-md-6">
               
                  <div class="panel panel-primary">
                    <div class="panel-heading">
                      	<p>
            			<strong>Mission</strong>: to give all animals life outside of laboratories and inspire public discourse about the practice of vivisection through rescue efforts and public policy.</p>
                    	<p><strong>Vision</strong>: A world in which progress and innovation is achieved entirely through non-animal research methods. </p>				</div>
