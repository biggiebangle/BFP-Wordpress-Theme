       <p> Another effective way to donate to Beagle Freedom Project is through a gift of life insurance, which, depending on how the donation is accomplished, can have tax advantages, as well as the benefit of generally avoiding probate, creditor's claims and contests by heirs.</p>
    
        <p>There are two basic ways to make a gift of life insurance: (1) an irrevocable gift of a new or existing policy where the donor gives up all incidents of ownership, or (2) by naming ARME (Animal Rescue, Media & Education) dba Beagle Freedom Project as the outright or contingent beneficiary of a policy. Each approach has advantages and disadvantages and you should discuss them with your lawyer, advisor and accountant.</p>
        
        <p>Irrevocable gift of an existing life insurance policy: If you own life insurance, you might consider making an irrevocable gift of the policy to Beagle Freedom Project. If complete ownership is transferred to Beagle Freedom Project and BFP is named as the beneficiary, the gift will generate a charitable income tax deduction.</p>
        
        <p>Irrevocable gift of a new policy: You may take out a new policy and irrevocably name Beagle Freedom Project as the owner and the beneficiary. Whether you make one single premium payment for the policy or pay premiums annually, each payment produces a charitable income tax deduction.</p>
        
       <p> Naming the charity as a primary or contingent beneficiary: You may also name Beagle Freedom Project as either the primary or contingent beneficiary of the policy. However, this option will not produce an income tax charitable deduction for the payment of future premiums on the policy, but it does provide a full estate tax charitable deduction at death. </p>
        
