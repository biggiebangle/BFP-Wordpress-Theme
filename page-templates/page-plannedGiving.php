        <div role="tabpanel" class="tab-pane fade" id="trusts">
        <p>Another method to donate to Beagle Freedom Project is through a species of charitable remainder trust. A charitable remainder trust is a trust which provides for: (1) a definite distribution, at least annually; (2) to one or more beneficiaries, at least one of which is not a charity, such as BFP; (3) for life or for a term of years; (4) with an irrevocable remainder interest to be held for the benefit of, or paid over to, charity. </p>
    
        <p>There are two types of charitable remainder trusts: a (1) charitable remainder annuity trust, or a (2) charitable remainder unitrust.  Whether a charitable remainder trust is a charitable remainder annuity trust or a charitable remainder unitrust depends essentially on how the specified distributions to the beneficiaries are determined. In the case of an charitable remainder annuity trust, the specified distribution must be a sum certain that is not less than five percent of the initial fair market value of all assets placed in trust. In the case of a unitrust, the specified distribution must be a fixed percentage that is not less than five percent of the net fair market value of the trust assets, valued annually.</p> 
        
        <p>The IRS typically issues a series of revenue ruling and revenue procedures that provide specimen (sample) trust agreements for various types of charitable remainder trusts. When a trust is drafted that contains provisions substantially similar to those provided in the appropriate sample, the IRS may recognize the trust as satisfying all the requirements of the law and and regulations. Consequently, your attorney, or whatever professional assists you in drafting your trust agreement, might not have to request a ruling from the IRS on the qualifications of your charitable remainder trust as drafted.</p>
        
        <p>A charitable remainder trust has several benefits. The client can control the distribution of her/his social capital by naming the charitable remainderman of the trust. As opposed to an outright gift to charity, a charitable remainder trust allows the client grantor to make a charitable gift while retaining some benefit, either for him/herself or others, from the property transferred.  Also, no capital gain taxes are due on the transfer of the assets into the charitable remainder trust. This is because when the property in the trust is sold, the trust does not have to pay any capital gain taxes. This gain, in turn, might be used to benefit income beneficiaries for several years.</p>
        
        <p>The charitable remainder trust also allows for a fair amount of flexibility in selecting the timing and the pay-out of the income stream, permitting it to be distributed during the client's lifetime or for a period of years. This payout, then, can be specifically tailored to meet each client's individual needs.</p>
        
       <p> Finally, certain income and estate tax deductions may be generated by the creation of a charitable remainder trust.</p>
        
        <p>Talk to your estate planner or attorney to discuss donating to Beagle Freedom Project through a charitable remainder trust.</p>
