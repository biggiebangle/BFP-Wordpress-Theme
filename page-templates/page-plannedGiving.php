            <p>Retirement plans are among the most highly taxed assets one can own, subject to income tax and estate tax that can deplete the value significantly. By naming Beagle Freedom Project as the beneficiary of your retirement plan, the retirement funds remaining at your death are not taxed and your heirs have no "hidden" income tax liability. 
        
           <p> Another very efficient way to give retirement plan assets to Beagle Freedom Project is through the IRA Charitable Rollover.  Under the IRA Charitable Rollover, for eligible tax years, individuals age 70½ or over could exclude up to $100,000 from gross income for donations paid directly to a qualified charity from their IRA. Key points about qualified charitable contributions (QCDs) include:</p>
         <ul>   
                <li>Married individuals filing a joint return could exclude up to $100,000 donated from each spouse's own IRA ($200,000 total).</li>
                <li>The donation satisfies any IRA required minimum distributions for the year. </li>
                <li>The amount excluded from gross income isn't deductible.</li>
                <li>Donations from an inherited IRA are eligible if the beneficiary is at least age 70 ½.</li>
                <li>Donations from a SEP or SIMPLE IRA are not eligible.</li>
                <li> Donations from a Roth IRA are also eligible.</li>
          </ul>  
           <p> As in all charitable gift decision-making, review with one's advisor and attorney is encouraged.</p>
        
